function getNatureStr(nature)
    local eff = NATURE_EFFECTS[nature]
    if eff and eff ~= "" then
        return string.format("%s (%s)", nature, eff)
    end
    return nature or "Unknown"
end

function readBoxMon(address)
	local mon = {}
	mon.personality = emu:read32(address + 0)
	mon.otId = emu:read32(address + 4)
	mon.nickname = toString(emu:readRange(address + 8, 10))
	
	-- Decryption
	local key = mon.otId ~ mon.personality
	local substructSelector = {
		[ 0] = {0, 1, 2, 3}, [ 1] = {0, 1, 3, 2}, [ 2] = {0, 2, 1, 3}, [ 3] = {0, 3, 1, 2},
		[ 4] = {0, 2, 3, 1}, [ 5] = {0, 3, 2, 1}, [ 6] = {1, 0, 2, 3}, [ 7] = {1, 0, 3, 2},
		[ 8] = {2, 0, 1, 3}, [ 9] = {3, 0, 1, 2}, [10] = {2, 0, 3, 1}, [11] = {3, 0, 2, 1},
		[12] = {1, 2, 0, 3}, [13] = {1, 3, 0, 2}, [14] = {2, 1, 0, 3}, [15] = {3, 1, 0, 2},
		[16] = {2, 3, 0, 1}, [17] = {3, 2, 0, 1}, [18] = {1, 2, 3, 0}, [19] = {1, 3, 2, 0},
		[20] = {2, 1, 3, 0}, [21] = {3, 1, 2, 0}, [22] = {2, 3, 1, 0}, [23] = {3, 2, 1, 0},
	}

	local pSel = substructSelector[mon.personality % 24] or {0,1,2,3}
	local ss = {}
	for i = 0, 3 do
		ss[i] = {}
		for j = 0, 2 do
			ss[i][j] = emu:read32(address + 32 + pSel[i+1] * 12 + j * 4) ~ key
		end
	end

	-- Substruct 0: Growth
	mon.species = ss[0][0] & 0xFFFF
	mon.heldItem = ss[0][0] >> 16
    
	-- Substruct 1: Attacks
	mon.moves = {
		ss[1][0] & 0xFFFF, ss[1][0] >> 16,
		ss[1][1] & 0xFFFF, ss[1][1] >> 16
	}
    
    -- Substruct 3: Misc (IVs)
	local misc2 = ss[3][1]
    -- Revert logic to match export_battle.lua provided by user
    mon.hpIV = (misc2 >> 1) & 0x1F  
	mon.atkIV = (misc2 >> 6) & 0x1F
	mon.defIV = (misc2 >> 11) & 0x1F
	mon.speIV = (misc2 >> 16) & 0x1F
	mon.spaIV = (misc2 >> 21) & 0x1F
	mon.spdIV = (misc2 >> 26) & 0x1F
    
    -- Nature Calculation
    mon.hiddenNature = (ss[0][2] >> 16) & 0x1F 
    local natures = {
		"Hardy","Lonely","Brave","Adamant","Naughty",
		"Bold","Docile","Relaxed","Impish","Lax",
		"Timid","Hasty","Serious","Jolly","Naive",
		"Modest","Mild","Quiet","Bashful","Rash",
		"Calm","Gentle","Sassy","Careful","Quirky"
    }

    if mon.hiddenNature == 26 or mon.hiddenNature == nil then
        mon.nature = natures[(mon.personality % 25) + 1]
    else
        mon.nature = natures[mon.hiddenNature + 1] or natures[(mon.personality % 25) + 1]
    end
    
    mon.natureStr = getNatureStr(mon.nature)
    mon.abilityBit = mon.personality & 1
	return mon
end

function readPartyMon(address)
	local mon = readBoxMon(address)
	mon.status = emu:read32(address + 80)
	mon.level = emu:read8(address + 84)
	mon.currentHp = emu:read16(address + 86)
	mon.maxHp = emu:read16(address + 88)
    
    mon.atk = emu:read16(address + 90)
    mon.def = emu:read16(address + 92)
    mon.spe = emu:read16(address + 94)
    mon.spa = emu:read16(address + 96)
    mon.spd = emu:read16(address + 98)
    
	return mon
end

-- Helper for Status
function getStatusString(val)
    if val == 0 then return "" end
    if (val & 8) ~= 0 then return "[PSN]" end
    if (val & 16) ~= 0 then return "[BRN]" end
    if (val & 32) ~= 0 then return "[FRZ]" end
    if (val & 64) ~= 0 then return "[PAR]" end
    if (val & 128) ~= 0 then return "[TOX]" end
    local slp = val & 7
    if slp > 0 then return string.format("[SLP:%d]", slp) end
    return string.format("[ST:%d]", val) -- Fallback
end

function getStageStr(val)
    local diff = (val or 6) - 6
    if diff == 0 then return "" end
    if diff > 0 then return string.format("(+%d)", diff) end
    return string.format("(%d)", diff)
end

function readBattleMon(index)
    local address = BATTLE_MON_START + (index * 0x5C)
    local mon = {}
    mon.species = emu:read16(address + 0x00)
    mon.atk, mon.def, mon.spe, mon.spa, mon.spd = emu:read16(address+0x02), emu:read16(address+0x04), emu:read16(address+0x06), emu:read16(address+0x08), emu:read16(address+0x0A)
    mon.moves = {emu:read16(address+0x0C), emu:read16(address+0x0E), emu:read16(address+0x10), emu:read16(address+0x12)}
    mon.statStages = {hp=emu:read8(address+0x18),atk=emu:read8(address+0x19),def=emu:read8(address+0x1A),spe=emu:read8(address+0x1B),spa=emu:read8(address+0x1C),spd=emu:read8(address+0x1D),acc=emu:read8(address+0x1E),eva=emu:read8(address+0x1F)}
    mon.ability = emu:read8(address + 0x20)
    mon.currentHp, mon.maxHp, mon.level = emu:read16(address+0x2A), emu:read16(address+0x2E), emu:read8(address+0x2C)
    mon.nickname = toString(emu:readRange(address+0x30, 10))
    mon.personality = emu:read32(address+0x48)
    mon.status = emu:read32(address+0x50)
    return mon
end

function writeMonInternal(buffer, mon)
    table.insert(buffer, string.format('      "speciesId": %d,\n', mon.species))
    table.insert(buffer, string.format('      "nickname": "%s",\n', mon.nickname:gsub('"', '\\"')))
    table.insert(buffer, string.format('      "level": %d,\n', mon.level))
    table.insert(buffer, string.format('      "currentHp": %d,\n', mon.currentHp))
    table.insert(buffer, string.format('      "maxHp": %d,\n', mon.maxHp))
    table.insert(buffer, string.format('      "item": %d,\n', mon.heldItem or 0))
    table.insert(buffer, string.format('      "nature": "%s",\n', mon.nature or "Unknown"))
    if mon.hpIV then
         table.insert(buffer, string.format('      "ivs": {"hp":%d,"atk":%d,"def":%d,"spe":%d,"spa":%d,"spd":%d},\n', mon.hpIV, mon.atkIV, mon.defIV, mon.speIV, mon.spaIV, mon.spdIV))
    end
    
    table.insert(buffer, '      "moves": [')
    for i = 1, 4 do table.insert(buffer, mon.moves[i]); if i < 4 then table.insert(buffer, ", ") end end
    table.insert(buffer, '],\n')
    if mon.atk then
        table.insert(buffer, '      "stats": {')
        table.insert(buffer, string.format('"hp": %d, "atk": %d, "def": %d, "spa": %d, "spd": %d, "spe": %d', mon.maxHp, mon.atk, mon.def, mon.spa, mon.spd, mon.spe))
        table.insert(buffer, '}')
    end
    if mon.statStages then
        table.insert(buffer, ',\n      "statStages": {')
        table.insert(buffer, string.format('"atk": %d, "def": %d, "spe": %d, "spa": %d, "spd": %d, "acc": %d, "eva": %d', mon.statStages.atk, mon.statStages.def, mon.statStages.spe, mon.statStages.spa, mon.statStages.spd, mon.statStages.acc, mon.statStages.eva))
        table.insert(buffer, '}')
    end
end

function exportBattle()
    local buffer = {}
    table.insert(buffer, '{\n  "player_side": {\n    "active": {\n')
    writeMonInternal(buffer, readBattleMon(0))
    table.insert(buffer, '\n    },\n    "party": [\n')
    local pCount = emu:read8(PLAYER_PARTY_COUNT)
    local first = true
    for i = 0, 5 do
        if i < pCount then
            local mon = readPartyMon(PLAYER_PARTY_LOC + (i * partyMonSize))
            if mon.species > 0 then
                if not first then table.insert(buffer, ',\n') end
                table.insert(buffer, '    {\n'); writeMonInternal(buffer, mon); table.insert(buffer, '\n    }')
                first = false
            end
        end
    end
    table.insert(buffer, '\n    ]\n  },\n  "opponent_side": {\n    "active": {\n')
    writeMonInternal(buffer, readBattleMon(1))
    table.insert(buffer, '\n    },\n    "party": [\n')
    first = true
    for i = 0, 5 do
        local addr = ENEMY_PARTY_LOC + (i * partyMonSize)
        if emu:read32(addr) ~= 0 then
            local mon = readPartyMon(addr)
            if mon.species > 0 and mon.species < 2000 then
                if not first then table.insert(buffer, ',\n') end
                table.insert(buffer, '    {\n'); writeMonInternal(buffer, mon); table.insert(buffer, '\n    }')
                first = false
            end
        end
    end
    table.insert(buffer, '\n    ]\n  }\n}\n')
    local currentJSON = table.concat(buffer)
    
    if currentJSON ~= lastExportedJSON then
        local file = io.open(OUTPUT_FILE, "w")
        if file then
            file:write(currentJSON)
            file:close()
            lastExportedJSON = currentJSON
        end
    end
end

function readPredictions()
    local file = io.open(INPUT_FILE, "r")
    if not file then return nil end
    local content = file:read("*all")
    file:close()
    return content
end

